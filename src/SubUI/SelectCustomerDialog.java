    private void loadCustomerTable() {

        try {

            int count = 0;

            ResultSet resultSet = MySql.execute("SELECT * FROM `customer` WHERE `mobile` LIKE '%" + jTextField2.getText() + "%' AND `username` LIKE '%" + jTextField1.getText() + "%'");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> v = new Vector<>();
                count++;

                v.add(resultSet.getString("nic"));
                v.add(String.valueOf(count));
                v.add(resultSet.getString("username"));
                v.add(resultSet.getString("mobile"));
                v.add(resultSet.getString("due_payments"));
                v.add(resultSet.getString("points"));

                model.addRow(v);
            }

        } catch (Exception e) {
            utils.Logger.logger.log(Level.WARNING, "SelectCustomer loadCustomerTable", e);
        }

    }

    public void setCustomer(String name, String mobile, String customerID, double balance, int poitns) {

        if (invoice != null) {
            this.invoice.setCustomer(name, mobile, customerID, balance, poitns);
        } else if (invoicehistory != null) {

            this.invoicehistory.setCustomer(name, mobile, customerID);
        }

    }

